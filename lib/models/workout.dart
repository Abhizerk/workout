class Workout {
  final String exercise;
  final double duration;
  final double calories;
  final String type;
  final String day;
  final String time;
  final String notes;

  Workout({
    required this.exercise,
    required this.duration,
    required this.calories,
    required this.type,
    required this.day,
    required this.time,
    required this.notes,
  });
}
